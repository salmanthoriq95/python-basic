# Arithmetic

## Add
a = 10
b = 3
c = 10 + 3
print(a, " + ", b, " = ", c)

## Subtrac
a = 10
b = 3
c = 10 - 3
print(a, " - ", b, " = ", c)

## Multipication
a = 10
b = 3
c = 10 * 3
print(a, " * ", b, " = ", c)

## Divide
a = 10
b = 3
c = 10 / 3
print(a, " / ", b, " = ", c)

## modulus
a = 10
b = 3
c = 10 % 3
print(a, " % ", b, " = ", c)

## floor division (pembulatan angka ke bawah)
a = 10
b = 3
c = 10 // 3
print(a, " // ", b, " = ", c)

## exponent
a = 10
b = 3
c = 10 ** 3
print(a, " ** ", b, " = ", c)

## square root
a = 16
b = 1 / 2
c = a ** b
print("2V", a, " = ", c)

